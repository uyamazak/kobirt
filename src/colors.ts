function* numberGen(length: number): Generator<number> {
  let i = 0
  while (true) {
    i++
    if (i === length) {
      i = 0;
    }
    yield i
  }
}
const gen = numberGen(12)
export const getPastelColors = (): string[] => {
  const i = gen.next()
  const value = i.value
  return pastel.map((v) => v[value])
}
